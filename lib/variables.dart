/*
* This program declares a variable 'x', initializes with an int and prints to
* to console. Then the value 10 is printed. */

void main() {
  //Variable declaration.
  var x;
  //All variables are initially null, which means no value.

  //Initializing x with an integer.
  x = 5;

  //Printing the variable x.
  print(x);
  //Printing the letter x.
  print("x");
  //Printing the number 10.
  print(10);
}
